def floordivision(a,b):
    answer = a//b
    return answer

def modulus(x,y):
    answer = x%y
    return answer

def addition(u,v):
    result = u+v
    return result

def subtraction(m,n):
    answer = m-n
    return answer

def multiply(s,t):
    answer = s*t
    return answer

def division(j,k):
    answer = j/k
    return answer